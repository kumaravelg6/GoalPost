import UIKit

extension UIButton {
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.05642852583, green: 0.7607115977, blue: 0.0001462485061, alpha: 1)
    }
    
    func setDeselctedColor() {
        self.backgroundColor = #colorLiteral(red: 0.5755004186, green: 0.9014357411, blue: 0.5867855437, alpha: 1)
    }
    
}
